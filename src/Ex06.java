
public class Ex06 {

	public static void main(String[] args) {
		// 자료형 : 저장하고자 하는 리터널의 type
		/// 리터널(자료형) : 정수(int, long), 실수(float, double), 문자(char), 문자열(String)
		
		int i = 10;
		System.out.println(i);
		
		double d = 0.5 ; 
		System.out.println(d);
		
		char ch = 'A';
		System.out.println(ch);
		
		String str = "abcd";
		System.out.println(str);
		
		// 4byte 이상을 저장하기 위한 자료형 : long
		// 범위  : -2147483648 ~ 214748647
		long l1 = 2147493648L;
		System.out.println(l1);
		
		l1 = 1234567890123L;
		System.out.println(l1);
	}

}
