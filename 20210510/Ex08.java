
public class Ex08 {

	public static void main(String[] args) {
		// 부정 연산자  = !
		// 논리형(부울) 타입 : true, false
		boolean b = true;
		System.out.println(b);
		System.out.println(!b);  // false
		System.out.println(!!b); // true
		
		b= false;
		System.out.println(b);
		System.out.println(!b);  // true
		System.out.println(!!b); // false
		
		
	}

}
