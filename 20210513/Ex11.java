
public class Ex11 {
	private String kind;
	private String name;
	private String color;
	
	public void setKind(String kind) {
		this.kind = kind;
	}
	public void setName(String name) {
		this.name = name;
	}
	public void setColor(String color) {
		this.color = color;
	}
	
	public String getKind() {
		return kind;
	}
	public String getName() {
		return name;
	}
	public String getColor() {
		return color;
	}
}
